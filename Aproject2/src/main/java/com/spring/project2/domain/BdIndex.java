package com.spring.project2.domain;

public class BdIndex {
	private int bdDate;
	private String district;
	private float overcrowdingAvg;
	
	public int getBdDate() {
		return bdDate;
	}
	public void setBdDate(int bdDate) {
		this.bdDate = bdDate;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public float getOvercrowdingAvg() {
		return overcrowdingAvg;
	}
	public void setOvercrowdingAvg(float overcrowdingAvg) {
		this.overcrowdingAvg = overcrowdingAvg;
	}
	
	
}
